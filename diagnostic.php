  A natural electrical system causes the heart muscle to contract and pump blood through the heart to the lungs and the rest of the body. Electrocardiography (ECG) is a test that records the electrical activity of the heart.<br /><br />

The electrical activity of the heart can be detected through the skin by small metal discs called electrodes. During electrocardiography, the electrodes are attached to the skin on the chest, arms, and legs. The electrodes are also connected to a machine that translates the electrical activity into line tracings on paper. These tracings are often analyzed by the machine and then carefully reviewed by a doctor for abnormalities.<br /><br />
