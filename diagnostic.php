It is the examination of any part of body for diagnostic purposes by means of x-rays, the record of the findings being impressed upon a photographic plate or film.<br /><br />

X – rays: X-rays are a form of radiation, like light or radio waves that can be focused into a beam, much like a flashlight beam. Unlike a beam of light, however, X –rays can pass through most objects, including the human body. When X-rays strike a piece of photographic film, they can produce a picture. Dense tissues in the body, such as bones, block (absorb) many of the X-rays and appear white on an X-ray picture. Less dense tissues, such as muscles and organs, block fewer of the X-rays (more of the X-rays pass through) and appear in shades of gray. X-rays that pass through air appear black.<br /><br />

X –ray is one of the fastest and easiest ways for a physician to view the internal organs and structures of the body. X-Ray is an excellent tool for assessing skeletal trauma (eg. Broken bones), for diagnosing the gastro-intestinal system (digestive tract) and for comprehensive imaging of the thoracic cavity including the lungs and heart.<br /><br /> 
